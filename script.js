document.addEventListener("DOMContentLoaded", () => {
  // --- Global State ---
  let currentLang = "en";

  // --- Translation Engine ---
  function updateLanguage(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ur" ? "rtl" : "ltr";
    
    // Update body class for styling targets (e.g. Urdu font family, RTL adjustments)
    if (lang === "ur") {
      document.body.classList.add("ur-mode");
    } else {
      document.body.classList.remove("ur-mode");
    }

    // Translate all elements with data-translate attribute
    const elements = document.querySelectorAll("[data-translate]");
    elements.forEach(el => {
      const key = el.getAttribute("data-translate");
      if (window.translations[lang] && window.translations[lang][key]) {
        // If it's an input/textarea with placeholder, translate placeholder instead
        if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
          el.setAttribute("placeholder", window.translations[lang][key]);
        } else {
          el.innerHTML = window.translations[lang][key];
        }
      }
    });

    // Update the simulator display
    updateSimulator();

    // Toggle active state on language buttons
    const langBtn = document.getElementById("lang-toggle-btn");
    if (langBtn) {
      langBtn.textContent = lang === "en" ? "اردو" : "English";
    }
  }

  // Language switch toggle listener
  const langBtn = document.getElementById("lang-toggle-btn");
  if (langBtn) {
    langBtn.addEventListener("click", () => {
      const nextLang = currentLang === "en" ? "ur" : "en";
      updateLanguage(nextLang);
    });
  }

  // --- Program Filter Tabs ---
  const filterTabs = document.querySelectorAll(".tab-btn");
  const programCards = document.querySelectorAll(".program-card");

  filterTabs.forEach(tab => {
    tab.addEventListener("click", () => {
      // Remove active class from all tabs
      filterTabs.forEach(t => t.classList.remove("active"));
      // Add active class to clicked tab
      tab.classList.add("active");

      const category = tab.getAttribute("data-category");

      programCards.forEach(card => {
        if (category === "all" || card.getAttribute("data-category") === category) {
          card.style.display = "block";
          // Quick fade-in animation
          card.style.opacity = "0";
          setTimeout(() => {
            card.style.opacity = "1";
          }, 50);
        } else {
          card.style.display = "none";
        }
      });
    });
  });

  // --- Donation Simulator Hook ---
  const donationSlider = document.getElementById("donation-range");
  const donationValueDisplay = document.getElementById("donation-value");
  const donationImpactDisplay = document.getElementById("donation-impact");

  function updateSimulator() {
    if (!donationSlider || !donationValueDisplay || !donationImpactDisplay) return;

    const value = parseInt(donationSlider.value);
    
    // Format currency based on language
    const formattedVal = currentLang === "ur" 
      ? `روپے ${value.toLocaleString("ur-PK")}`
      : `Rs. ${value.toLocaleString("en-PK")}`;

    donationValueDisplay.textContent = formattedVal;
    
    // Fetch calculated impact text from simulatorData
    const impactText = window.getImpactForAmount(value, currentLang);
    donationImpactDisplay.textContent = impactText;
  }

  if (donationSlider) {
    donationSlider.addEventListener("input", updateSimulator);
    
    // Quick donation tier buttons
    const tierBtns = document.querySelectorAll(".tier-btn");
    tierBtns.forEach(btn => {
      btn.addEventListener("click", () => {
        const val = btn.getAttribute("data-value");
        donationSlider.value = val;
        updateSimulator();
      });
    });
  }

  // --- Reveal on Scroll (Intersection Observer) ---
  const revealElements = document.querySelectorAll(".reveal-on-scroll");
  const revealObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target); // Trigger once
      }
    });
  }, {
    threshold: 0.1,
    rootMargin: "0px 0px -50px 0px"
  });

  revealElements.forEach(el => revealObserver.observe(el));

  // --- Dynamic Number Counter Animation ---
  const statValElements = document.querySelectorAll(".stat-val");
  const countObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        animateCounters();
        observer.disconnect(); // Trigger only once for the page
      }
    });
  }, { threshold: 0.5 });

  const statsSection = document.getElementById("impact-counters");
  if (statsSection) {
    countObserver.observe(statsSection);
  }

  function animateCounters() {
    statValElements.forEach(el => {
      const targetStr = el.getAttribute("data-target");
      const hasPlus = targetStr.includes("+");
      const hasYrs = targetStr.toLowerCase().includes("yrs") || targetStr.includes("سال");
      let targetNum = parseInt(targetStr.replace(/[^0-9]/g, ""));
      
      let start = 0;
      const duration = 2000; // 2 seconds
      const startTime = performance.now();

      function updateCount(currentTime) {
        const elapsed = currentTime - startTime;
        const progress = Math.min(elapsed / duration, 1);
        // Easing out quadratic
        const easeProgress = progress * (2 - progress);
        const currentValue = Math.floor(easeProgress * targetNum);

        let displayVal = currentValue.toLocaleString();
        
        if (currentLang === "ur") {
          if (hasPlus) displayVal = `+${displayVal}`;
          if (hasYrs) displayVal = `${currentValue} سال`;
        } else {
          if (hasPlus) displayVal = `${displayVal}+`;
          if (hasYrs) displayVal = `${currentValue} Yrs`;
        }

        el.textContent = displayVal;

        if (progress < 1) {
          requestAnimationFrame(updateCount);
        } else {
          // Double check final value is formatted correctly with proper labels
          el.textContent = targetStr;
          // Trigger dynamic check just to align with state
          if (currentLang === "ur") {
            const key = el.getAttribute("data-translate");
            if (key && window.translations.ur[key]) {
              el.textContent = window.translations.ur[key];
            }
          }
        }
      }
      requestAnimationFrame(updateCount);
    });
  }

  // --- Theme Toggle ---
  const themeBtn = document.getElementById("theme-toggle-btn");
  if (themeBtn) {
    if (localStorage.getItem("theme") === "dark") {
      document.body.classList.add("dark-mode");
      themeBtn.innerHTML = '<i class="fa-solid fa-sun"></i>';
    }
    themeBtn.addEventListener("click", () => {
      document.body.classList.toggle("dark-mode");
      if (document.body.classList.contains("dark-mode")) {
        localStorage.setItem("theme", "dark");
        themeBtn.innerHTML = '<i class="fa-solid fa-sun"></i>';
      } else {
        localStorage.setItem("theme", "light");
        themeBtn.innerHTML = '<i class="fa-solid fa-moon"></i>';
      }
    });
  }

  // --- Initialize Webpage ---
  updateLanguage("en");
});
